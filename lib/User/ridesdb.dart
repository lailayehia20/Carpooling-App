class Ride {
  final String destination;
  final String pickup;
  final String fare;
  final String time;
  final String date;
  final String meetingPoint;
  final String driverId;
  final String rideId;


  Ride({
    required this.destination,
    required this.pickup,
    required this.fare,
    required this.time,
    required this.date,
    required this.meetingPoint,
    required this.driverId,
    required this.rideId,
  });
}